// Seleciona o elemento HTML com o ID "paragrafo"
let paragrafo = document.getElementById("paragrafo")

// Exibe no console o elemento selecionado
console.log(paragrafo)

// Seleciona o primeiro elemento HTML <h1> encontrado na página
let titulo = document.getElementsByTagName('h1')[0]

// Exibe no console o título selecionado
console.log(titulo)

// Seleciona todos os elementos HTML com a classe CSS "item"
let itens_lista = document.getElementsByClassName('item')

// Exibe no console os itens da lista selecionados
console.log(itens_lista)

// Seleciona todos os elementos HTML <li> encontrados na página
let lista = document.getElementsByTagName("li")

// Exibe no console a lista de elementos <li> selecionados
console.log(lista)
