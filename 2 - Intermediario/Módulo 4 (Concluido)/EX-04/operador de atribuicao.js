// Define duas variáveis numéricas
var num_1 = 5;
var num_2 = 5;

// Soma e atribuição
console.log(num_1 = num_1 + num_2); // Saída: 10 (5 + 5)
console.log(num_1 += num_2); // Saída: 15 (10 + 5)

// Subtração e atribuição
console.log(num_1 -= num_2); // Saída: 10 (15 - 5)

// Multiplicação e atribuição
console.log(num_1 *= num_2); // Saída: 50 (10 * 5)

// Divisão e atribuição
console.log(num_1 /= num_2); // Saída: 10 (50 / 5)

// Incremento e decremento
var x = 5;
var y = 10;

x++; // Incremento de x
y--; // Decremento de y

// Imprime os valores de x e y
console.log(`${x} ${'\n'+ y}`); // Saída: 6 (depois do incremento) e 9 (depois do decremento)
