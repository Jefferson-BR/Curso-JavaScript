// Define a variável nome como 'Pedro'
var nome = 'Pedro';

// Verifica se o nome é igual a 'Jefferson'
if (nome == 'Jefferson') {
    // Se verdadeiro, imprime 'O Nome é Verídico'
    console.log('O Nome é Verídico');
}

// Verifica se o nome é diferente de 'Jefferson'
if (nome != 'Jefferson') {
    // Se verdadeiro, imprime 'O Nome foi alterado e não é verídico'
    console.log('O Nome foi alterado e não é verídico');
}

// Imprime 'Fim do programa de comparação' independentemente do valor de nome
console.log('Fim do programa de comparação');
