/*

- O while é uma estrutura de controle de fluxo em JavaScript que executa um bloco de código repetidamente enquanto uma condição específica for verdadeira. Ele avalia a condição antes de executar o bloco de código e continua a executar enquanto a condição permanecer verdadeira. Assim que a condição se tornar falsa, a execução do loop é interrompida.

- O do...while é uma estrutura de controle de fluxo em JavaScript que executa um bloco de código pelo menos uma vez e então continua a executá-lo enquanto uma condição específica for verdadeira. A condição é verificada após a primeira execução do bloco de código.

Diferença entre DO e While:

No do...while, o bloco de código é executado pelo menos uma vez, independentemente da condição ser verdadeira ou falsa. Depois que o bloco é executado pela primeira vez, a condição é verificada. Se a condição for verdadeira, o bloco será executado novamente e o processo se repetirá até que a condição se torne falsa.

No while, a condição é verificada antes da execução do bloco de código. Se a condição for falsa desde o início, o bloco nunca será executado. O bloco de código só será executado se a condição for verdadeira.

Portanto, a diferença fundamental é que o do...while garante a execução do bloco de código pelo menos uma vez, enquanto o while pode não executar o bloco de código se a condição for falsa desde o início.

*/